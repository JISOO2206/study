<template>
  <div>
    <div v-for="user in users" v-bind:key="user.title" >{{ user.title }}</div>
  </div>
</template>

<script>
import { fetchNewsList } from '@/api/index.js'


export default {
  data(){
    return {
      users: []
    }
  },
  created(){
    let vm = this;
    fetchNewsList().then(function (response){
      vm.users = response.data;
    })
    .catch(function(error){
      console.log(error);
    })
  }
}
</script>

